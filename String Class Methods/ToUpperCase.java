package stringclassmethods;

// In String the toUpperCase() method use to convert LowerCase String value to UpperCase String value.

public class ToUpperCase 

{

	public static void main(String[] args)
	
	{
		String Name="selva";
		
		System.out.println("(selva) convert to (SELVA) :"+Name.toUpperCase());//toUpperCase() method.
	}

}
